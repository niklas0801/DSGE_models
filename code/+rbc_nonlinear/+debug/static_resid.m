function [lhs, rhs] = static_resid(y, x, params)
T = NaN(3, 1);
lhs = NaN(13, 1);
rhs = NaN(13, 1);
T(1) = y(3)^params(1);
T(2) = y(5)*T(1);
T(3) = y(4)^(1-params(1));
lhs(1) = y(10);
rhs(1) = params(4)*y(2)^(-params(7));
lhs(2) = y(11);
rhs(2) = (-params(5))*(1-y(4))^(-params(8));
lhs(3) = y(13);
rhs(3) = params(1)*y(1)/y(3);
lhs(4) = y(12);
rhs(4) = y(1)*(1-params(1))/y(4);
lhs(5) = y(10);
rhs(5) = y(10)*params(2)*(1-params(3)+y(6));
lhs(6) = y(7);
rhs(6) = (-y(11))/y(10);
lhs(7) = y(3);
rhs(7) = y(3)*(1-params(3))+y(8);
lhs(8) = y(1);
rhs(8) = y(2)+y(8);
lhs(9) = y(1);
rhs(9) = T(2)*T(3);
lhs(10) = y(9);
rhs(10) = 1;
lhs(11) = y(7);
rhs(11) = y(12);
lhs(12) = y(6);
rhs(12) = y(13);
lhs(13) = log(y(5));
rhs(13) = log(y(5))*params(6)+x(1);
end
